'''
Algorithm
1. count 만큼 반복하면서 price*count를 누적해서 더함
2. 반복이 끝난 후 총 금액과 money의 차액 반환
'''

def solution(price, money, count):
    answer = 0
    # 놀이기구 이용료 계산
    for i in range(1, count+1):
        answer += price * i
    # 이용료와 money의 차액 계산
    answer -= money
    # 금액이 부족하지 않으면 0 return
    if answer <= 0:
        return 0
    return answer

print(solution(3, 20, 4)) # 10