def solution(n):
    answer = []
    list_ = [4, 1, 2]
    answer.insert(0, str(list_[n%3]))

    temp = n // 3
    if n % 3 == 0:
        temp -= 1
    if temp == 0:
        return ''.join(answer)

    answer.insert(0, str(list_[temp % 3]))

    return ''.join(answer)

