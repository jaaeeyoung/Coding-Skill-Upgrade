def solution(number, k):
    num = list(number[:])
    i = 0
    while True:
        if len(num[i]) >= len(number) - k:
            break
        else:
            index_ = 0
            temp = list(number[:])
            for j in num[i]:
                index_ += temp.index(j)
                temp = temp[temp.index(j) + 1:]
                            
            for j in temp:
                if num[i] + j not in num:
                    num.append(num[i] + j)
        i += 1
                        
    
    # 정수형으로 변환
    num = set(map(int, num))
    return str(max(num))

